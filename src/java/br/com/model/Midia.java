
package br.com.model;


public class Midia {
    private String name;
    private String director;
    private int year;
    private String plot;
    private String type;
    private String tema;
    private int faixa_etaria;
    private String url_img;
    private  String wallpaper;
    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the director
     */
    public String getDirector() {
        return director;
    }

    /**
     * @param director the director to set
     */
    public void setDirector(String director) {
        this.director = director;
    }

    /**
     * @return the year
     */
    public int getYear() {
        return year;
    }

    /**
     * @param year the year to set
     */
    public void setYear(int year) {
        this.year = year;
    }

    /**
     * @return the plot
     */
    public String getPlot() {
        return plot;
    }

    /**
     * @param plot the plot to set
     */
    public void setPlot(String plot) {
        this.plot = plot;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the tema
     */
    public String getTema() {
        return tema;
    }

    /**
     * @param tema the tema to set
     */
    public void setTema(String tema) {
        this.tema = tema;
    }

    /**
     * @return the faixa_etaria
     */
    public int getFaixa_etaria() {
        return faixa_etaria;
    }

    /**
     * @param faixa_etaria the faixa_etaria to set
     */
    public void setFaixa_etaria(int faixa_etaria) {
        this.faixa_etaria = faixa_etaria;
    }

    /**
     * @return the url_img
     */
    public String getUrl_img() {
        return url_img;
    }

    /**
     * @param url_img the url_img to set
     */
    public void setUrl_img(String url_img) {
        this.url_img = url_img;
    }

    /**
     * @return the wallpaper
     */
    public String getWallpaper() {
        return wallpaper;
    }

    /**
     * @param wallpaper the wallpaper to set
     */
    public void setWallpaper(String wallpaper) {
        this.wallpaper = wallpaper;
    }
}
